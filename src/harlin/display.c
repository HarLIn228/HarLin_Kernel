u32 display_rgb(u8 r, u8 g, u8 b)
{
    return ((u32)r << 16) | ((u32)g << 8) | (u32)b;
}

int display_get_width(void)
{
    if (current_mode == HARLIN_DISP_VESA)
        return vesa_width;
    if (current_mode == HARLIN_DISP_VGA_13H)
        return 320;
    return 80;
}

int display_get_height(void)
{
    if (current_mode == HARLIN_DISP_VESA)
        return vesa_height;
    if (current_mode == HARLIN_DISP_VGA_13H)
        return 200;
    return 25;
}

void display_draw_rect(int x, int y, int w, int h, u32 color)
{
    int i, j;
    if (current_mode == HARLIN_DISP_VGA_13H) {
        unsigned char* vga = (unsigned char*)0xA0000;
        unsigned char c8 = (unsigned char)(color & 0xFF);
        if (x < 0) { w += x; x = 0; }
        if (y < 0) { h += y; y = 0; }
        if (x >= 320 || y >= 200) return;
        if (x + w > 320) w = 320 - x;
        if (y + h > 200) h = 200 - y;
        if (w <= 0 || h <= 0) return;
        for (i = y; i < y + h; i++) {
            for (j = x; j < x + w; j++) {
                vga[i * 320 + j] = c8;
            }
        }
        return;
    }
    if (current_mode != HARLIN_DISP_VESA)
        return;
    if (x < 0) { w += x; x = 0; }
    if (y < 0) { h += y; y = 0; }
    if (x >= vesa_width || y >= vesa_height)
        return;
    if (x + w > vesa_width) w = vesa_width - x;
    if (y + h > vesa_height) h = vesa_height - y;
    if (w <= 0 || h <= 0)
        return;

    if (vesa_bpp == 32) {
        u32* lfb = (u32*)vesa_lfb;
        for (i = y; i < y + h; i++) {
            for (j = x; j < x + w; j++) {
                lfb[i * vesa_width + j] = color;
            }
        }
    } else {
        u8* lfb = (u8*)vesa_lfb;
        u8 r = (color >> 16) & 0xFF;
        u8 g = (color >> 8) & 0xFF;
        u8 b = color & 0xFF;
        for (i = y; i < y + h; i++) {
            for (j = x; j < x + w; j++) {
                int off = (i * vesa_width + j) * 3;
                lfb[off] = b;
                lfb[off + 1] = g;
                lfb[off + 2] = r;
            }
        }
    }
}

void display_draw_char(int x, int y, char c, u32 fg, u32 bg)
{
    int row, col;
    unsigned char ch = (unsigned char)c;
    if (current_mode == HARLIN_DISP_VGA_13H) {
        unsigned char* vga = (unsigned char*)0xA0000;
        unsigned char fg8 = (unsigned char)(fg & 0xFF);
        unsigned char bg8 = (unsigned char)(bg & 0xFF);
        if (ch >= 128) return;
        if (x < 0 || y < 0 || x + 8 > 320 || y + 16 > 200) return;
        for (row = 0; row < 16; row++) {
            unsigned char bits = font_8x16[ch][row];
            for (col = 0; col < 8; col++) {
                if (bits & (0x80 >> col))
                    vga[(y + row) * 320 + (x + col)] = fg8;
                else
                    vga[(y + row) * 320 + (x + col)] = bg8;
            }
        }
        return;
    }
    if (current_mode != HARLIN_DISP_VESA)
        return;
    if (ch >= 128)
        return;
    if (x < 0 || y < 0 || x + 8 > vesa_width || y + 16 > vesa_height)
        return;

    if (vesa_bpp == 32) {
        u32* lfb = (u32*)vesa_lfb;
        for (row = 0; row < 16; row++) {
            unsigned char bits = font_8x16[ch][row];
            for (col = 0; col < 8; col++) {
                if (bits & (0x80 >> col))
                    lfb[(y + row) * vesa_width + (x + col)] = fg;
                else
                    lfb[(y + row) * vesa_width + (x + col)] = bg;
            }
        }
    } else {
        u8* lfb = (u8*)vesa_lfb;
        u8 fr = (fg >> 16) & 0xFF, fg8 = (fg >> 8) & 0xFF, fb = fg & 0xFF;
        u8 br = (bg >> 16) & 0xFF, bg8 = (bg >> 8) & 0xFF, bb = bg & 0xFF;
        for (row = 0; row < 16; row++) {
            unsigned char bits = font_8x16[ch][row];
            for (col = 0; col < 8; col++) {
                int off = ((y + row) * vesa_width + (x + col)) * 3;
                if (bits & (0x80 >> col)) {
                    lfb[off] = fb; lfb[off + 1] = fg8; lfb[off + 2] = fr;
                } else {
                    lfb[off] = bb; lfb[off + 1] = bg8; lfb[off + 2] = br;
                }
            }
        }
    }
}

void display_draw_string(int x, int y, const char* str, u32 fg, u32 bg)
{
    int px = x;
    while (*str) {
        if (*str == '\n') {
            px = x;
            y += 16;
        } else {
            display_draw_char(px, y, *str, fg, bg);
            px += 8;
        }
        str++;
    }
}
